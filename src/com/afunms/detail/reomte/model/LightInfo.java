package com.afunms.detail.reomte.model;

public class LightInfo {

	/**
	 * 索引
	 */
	private String sindex;

	/**
	 * 描述
	 */
	private String descr;

	/**
	 * 信号灯颜色
	 */
	private String color;

	/**
	 * 类型
	 */
	private String type;

	/**
	 * 状态
	 */
	private String state;

	/**
	 * 信号灯显示颜色
	 */
	private String displycolor;

	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}

	/**
	 * @return the descr
	 */
	public String getDescr() {
		return descr;
	}

	/**
	 * @return the displycolor
	 */
	public String getDisplycolor() {
		return displycolor;
	}

	/**
	 * @return the sindex
	 */
	public String getSindex() {
		return sindex;
	}

	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param color
	 *            the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}

	/**
	 * @param descr
	 *            the descr to set
	 */
	public void setDescr(String descr) {
		this.descr = descr;
	}

	/**
	 * @param displycolor
	 *            the displycolor to set
	 */
	public void setDisplycolor(String displycolor) {
		this.displycolor = displycolor;
	}

	/**
	 * @param sindex
	 *            the sindex to set
	 */
	public void setSindex(String sindex) {
		this.sindex = sindex;
	}

	/**
	 * @param state
	 *            the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

}
